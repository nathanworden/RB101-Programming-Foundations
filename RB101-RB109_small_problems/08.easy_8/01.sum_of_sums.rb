# Problem
# Write a method that takes an array of numbers 
# Returns the sum of the sums of each leading subsequence for that Array.
# Assume that the Array always contains at least one number

# Examples / Test Cases
# sum_of_sums([3, 5, 2]) == (3) + (3 + 5) + (3 + 5 + 2) # -> (21)
# sum_of_sums([1, 5, 7, 3]) == (1) + (1 + 5) + (1 + 5 + 7) + (1 + 5 + 7 + 3) # -> (36)
# sum_of_sums([4]) == 4
# sum_of_sums([1, 2, 3, 4, 5]) == 35

# Data Structure
# Arrays, integers

# Algorithm
# define a method called 'sum_of_sums' which accepts an array as an argument.
# initiate an empty array called 'sums'
# initiate a variable called 'index' to 0
# initiate a loop
# initiate a counter to 0
# 

# Code

def sum_of_sums(array)
  sums = []
  index = 0
  loop do
    (0..index).each {|index| sums << array[index]}
    index += 1
    break if index == array.size
  end
  sums.reduce(:+)
end



