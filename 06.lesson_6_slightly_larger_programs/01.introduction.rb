Introduction
In this lesson, we're going to be working on slightly more complicated programs. These programs will be a departure from the simple programs in the previous lessons and are beyond what's necessary for the RB109 assessment. However, they'll challenge you to put everything you learned in the previous lessons to use and provide good practice for working with larger programs.

We suggest you take the following approach:

First, break down the problem into smaller pieces. This is probably the most challenging and most important part, because in order to break down the problem, you have to understand it well. Your first stab at breaking it down may not be appropriate. Don't be afraid to start over.

Map out the flow of the program in a flowchart, using sub-processes to encapsulate well-defined components of the problem.

When you're ready to tackle a component or sub-process, write out the pseudo-code for that sub-process only. It will most likely translate into a method (or several methods), so come up with clear inputs for the sub-process, and clear outputs you expect from the sub-process.

Play around with the code. Write every line of code and make sure you understand it. Don't copy/paste.

Do the assignments in sequence. We'll walk you through the necessary steps to break down the problem.

Don't be afraid to watch the walk-through videos. Use those as guard rails so you're not spinning your wheels stuck for too long. But don't just sit back and watch the videos without typing or coding along, either.

