# Introduction
# In this lesson, we'll challenge you to work through several Ruby programs of increasing difficulty. Along the way, you'll learn how to start thinking about the logic of a program, how to debug and get familiar with writing lots of code.

# Pre-requisites
# You should have Ruby installed and can execute Ruby programs, and know how to use irb.

# You should already be familiar with basic Ruby syntax and data structures (Arrays and Hashes), and know how to use git and push your code to Github.

# You should have completed the Prep course and all assignments in it. If not, you won't be ready for this course.

# Afterwards
# At the end of this lesson, you will not become a Ruby expert. You will, however, have gained some confidence in writing your first several Ruby programs. This is the first step to learning to program.